public class conversions {
    public static void main(String[] arg) {
        int a = 20;
        int b = 10;
        int x;
        System.out.println(x = a + b);//addition
        System.out.println(x = a - b);//sub
        System.out.println(x = a * b);//multi
        System.out.println(x = a / b);//div
        System.out.println(x = a % b);//module
    }

}
